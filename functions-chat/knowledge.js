function getRelevantKnowledge(context) {
  let blockKeys = CONTEXT_MAP[context] || [
    "deduction_general", "personal_loan_fees", "mortgage_rules", "pos_rules", "savings_rules"
  ];
  blockKeys = [...blockKeys, "founder_profile", "general_disclaimer"];
  let output = "\n\n---\n## معلومات إضافية لهذه الصفحة\n";
  for (const key of blockKeys) {
    if (SAMA_KNOWLEDGE_BLOCKS[key]) output += SAMA_KNOWLEDGE_BLOCKS[key] + "\n";
  }
  return output;
}

